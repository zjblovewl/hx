package cn.com.hxfz.model;

import java.math.BigDecimal;
import java.util.Date;

/**  
 * @类功能说明：佣金记录实体类
 * @公司名称：南京华讯方舟通讯设备有限公司  
 * @作者：qiangxuan  
 * @创建时间：2018年5月25日 上午10:40:12 
 * @版本：V1.0  
 */
public class CommissionRecord {
	
	private String id;
	
	private String withdrawalsId;
	
	private BigDecimal withdrawalsAmount;
	
	private Integer commissionRate;
	
	private BigDecimal commissionAmount;
	
	private Date createTime;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getWithdrawalsId() {
		return withdrawalsId;
	}

	public void setWithdrawalsId(String withdrawalsId) {
		this.withdrawalsId = withdrawalsId;
	}

	public BigDecimal getWithdrawalsAmount() {
		return withdrawalsAmount;
	}

	public void setWithdrawalsAmount(BigDecimal withdrawalsAmount) {
		this.withdrawalsAmount = withdrawalsAmount;
	}

	public Integer getCommissionRate() {
		return commissionRate;
	}

	public void setCommissionRate(Integer commissionRate) {
		this.commissionRate = commissionRate;
	}

	public BigDecimal getCommissionAmount() {
		return commissionAmount;
	}

	public void setCommissionAmount(BigDecimal commissionAmount) {
		this.commissionAmount = commissionAmount;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	
	
}
